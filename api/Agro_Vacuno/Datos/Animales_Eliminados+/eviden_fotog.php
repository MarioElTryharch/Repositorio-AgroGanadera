require_once __DIR__ . '/../../../../config/database.php';

$database = new Database();
$pdo = $database->getConnection();

if (!$pdo) {
    die(json_encode(["error" => "No se pudo conectar a la base de datos."]));
}

// ==========================================
// ðŸ”¹ LIMPIAR DATOS
// ==========================================
function limpiar($data) {
    return htmlspecialchars(trim($data));
}

// ==========================================
// ðŸ”¸ INSERTAR NUEVO REGISTRO
// ==========================================
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    try {
        $sql = "INSERT INTO fotos_eliminados (fotos_guardadas_elim)
                VALUES (:fotos_guardadas_elim)";
        $stmt = $pdo->prepare($sql);
        $stmt->execute([
            ':fotos_guardadas_elim'       => limpiar($_POST['fotos_guardadas_elim'] ?? 0),
        ]);

        

        echo json_encode(["success" => true, "message" => "âœ… Animal Eliminado agregado correctamente."]);
        exit;
    } catch (PDOException $e) {
        echo json_encode(["success" => false, "message" => "âŒ Error al guardar: " . $e->getMessage()]);
        exit;
    }
}

// ==========================================
// ðŸ”¹ OBTENER TODOS LOS REGISTROS (GET)
// ==========================================
if ($_SERVER['REQUEST_METHOD'] === 'GET') {
    try {
        $stmt = $pdo->query("SELECT * FROM fotos_eliminados ORDER BY id_eliminado DESC");
        $data = $stmt->fetchAll(PDO::FETCH_ASSOC);
        echo json_encode($data);
    } catch (PDOException $e) {
        echo json_encode(["error" => $e->getMessage()]);
    }
    exit;
}
